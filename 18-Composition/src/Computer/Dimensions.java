package Computer;

public class Dimensions {
	
	private int width;
	private int heigth;
	private int depth;
	
	
	public Dimensions(int width, int heigth, int depth) {
		this.width = width;
		this.heigth = heigth;
		this.depth = depth;
	}


	public int getWidth() {
		return width;
	}


	public int getHeigth() {
		return heigth;
	}


	public int getDepth() {
		return depth;
	}
	
	
	
	
	

}